# convert_and_save with explicit path
# ---------------------------------------------------------------------------


def test_convert_and_save_with_explicit_path(tmp_path, monkeypatch):
    """convert_and_save(path) should convert the fixture and return True."""
    import convert_data

    data_dir = tmp_path / "data"
    data_dir.mkdir()
    dest = data_dir / "sample_anonymized.xlsx"
    shutil.copy(FIXTURE, dest)

    out_csv = data_dir / "cleaned_master.csv"
    monkeypatch.setattr(convert_data, "OUTPUT_PATH", out_csv)
    monkeypatch.setattr(convert_data, "DATA_DIR", data_dir)

    result = convert_data.convert_and_save(dest)
    assert result is True
    assert out_csv.exists()
    df = pd.read_csv(out_csv)
    assert len(df) > 0


