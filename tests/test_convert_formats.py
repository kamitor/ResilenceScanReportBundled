def test_find_source_file_prefers_xlsx(tmp_path):
    """_find_source_file should prefer .xlsx over .xml when both are present."""
    import convert_data

    (tmp_path / "data.xlsx").write_bytes(b"")
    (tmp_path / "data.xml").write_text("<r/>", encoding="utf-8")
    result = convert_data._find_source_file(tmp_path)
    assert result is not None
    assert result.suffix == ".xlsx"


def test_find_source_file_finds_xml(tmp_path):
    """_find_source_file should find an .xml file when no Excel/ODS is present."""
    import convert_data

    xml_file = tmp_path / "data.xml"
    xml_file.write_text("<r/>", encoding="utf-8")
    result = convert_data._find_source_file(tmp_path)
    assert result is not None
    assert result.suffix == ".xml"


def test_find_source_file_returns_none_when_empty(tmp_path):
    """_find_source_file should return None for an empty directory."""
    import convert_data

    result = convert_data._find_source_file(tmp_path)
    assert result is None
